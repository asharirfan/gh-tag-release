export function printBanner(): void {
  if (!process.stdout.isTTY) {
    return;
  }

  const width = process.stdout.columns ?? 80;
  const banner = width >= 120 ? WIDE_BANNER : COMPACT_BANNER;
  const colorize = width >= 120 ? pc.white : pc.gray;

  console.log(colorize(banner.join("\n")));
  console.log("");
}
